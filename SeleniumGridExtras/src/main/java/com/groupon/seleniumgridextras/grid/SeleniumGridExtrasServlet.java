  private String readFile(String file) {
    String s = "";

    try {
      InputStream is = Thread.currentThread().getContextClassLoader().getResourceAsStream(file);
      s = IOUtils.toString(is, "UTF-8");
    } catch (Exception error) {
      System.out.println(error);
    }
    return s;
  }

